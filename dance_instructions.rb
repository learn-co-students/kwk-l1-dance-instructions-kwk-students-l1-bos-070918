end
def bob_the_reins
  puts "Do Step 1: Holding your arms out in front of you, straight and at chest level"
  puts "Step 2: Cross your right wrist over your left and hold them together"
  puts  "Step 3: Lift your arms up and down in a loose bouncing movement, in time with the beat of the song. This movement is repeated eight times."
end
def lasso 
  puts "Do Step 1: Start arm movement by holding left arm so the back of your wrist is near your chin, pointing your left elbow straight left."
  puts "Step 2: Lift right arm up and point right elbow diagonally to the right."
  puts "Step 3: Raise right forearm so that it points straight up, and whip it in small circles to the beat of the song, as if you were a cowboy holding a rope lasso. This movement is repeated eight times as well."
end
def skip_step(foot)
  puts "Briefly, Lower #{foot} foot to the ground."
  puts "Bounce #{foot} foot back up slightly, kicking it a few inches back."
  puts "#{foot}, Right, #{foot} and #{foot}"
end
